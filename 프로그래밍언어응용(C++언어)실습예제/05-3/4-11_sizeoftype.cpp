#include <iostream>
using namespace std;

int main()
{
  cout << "int 정수 타입 크기 = " << sizeof(int) << " bytes"
       << endl;
  cout << "double 부동 소수점 타입 크기 = "
       << sizeof(double) << " bytes" << endl;
  cout << "char 문자 타입 크기 = " << sizeof(char) << " byte"
       << endl;
  system("PAUSE");
  return  0;
}

